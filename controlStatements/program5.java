/* Take an integer as input and print wheather it is divisible by 4 or not
 * input : 5
 * output : Not Divisible
 */
class Divisible{

	public static void main(String[]args){
	
		int x = 5;
		if(x%4==0){
		
			System.out.println("Divisible");
		}else{
		
			System.out.println("Not Divisible");
		}
	}
}
