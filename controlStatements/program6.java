/* Given an intger input ,print wheather it is odd or even 
 * input : 7
 * output: 7 is odd
 * input : 4
 * output : 4 is even
 */
class Integer{

	public static void main(String[]args){
	
		int x = 7;
		if(x%2==0){
		
			System.out.println(x+" is even");
		}else{
		
			System.out.println(x+" is odd");
		}
	}
}

